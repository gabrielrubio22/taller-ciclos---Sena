    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
    
            txtAreaResult.setText("");
            String imputText = txtNum.getText().trim();
            if(!imputText.isEmpty()){
                
                try{
                    int num = Integer.parseInt(imputText);
                    String resultado = classyfyNumber(num, txtAreaResult);
                    txtAreaResult.setText(txtAreaResult.getText() + "\nEl numero: " + num + " es " +resultado);
                    
                } catch(NumberFormatException e){
                    txtAreaResult.append("Error introduzca un numero valido \n");
                }    
            } 
              else {
                txtAreaResult.append("Error introduzca un numero");
            }
            
    }//GEN-LAST:event_btnCalcularActionPerformed
    
    private String classyfyNumber (int a, javax.swing.JTextArea textArea ){
        
        int cont = 0;
        String result = "";
        String divisor;
        if( a > 0){
            for(int i = 1; i<a; i++){
                if(a % i == 0){
                    cont += i;
                    divisor = String.valueOf (i);
                    textArea.append(divisor + "\n");
                }
            }
        }
        else {
            textArea.append("EL NUMERO QUE INTRODUJO NO ES CORRECTO"+"\n"+" YA QUE DEBE SER MAYOR A 0");
            result = "Error";
        }
            
        
        if(result.equals("")){
            if(cont == a){
                result = "Perfecto";
            }
             else if (cont < a){
                result = "Deficiente";
            }
             else if (cont > a){
                result = "Abundante";
            }
        }
        
            return result;
    }
    
