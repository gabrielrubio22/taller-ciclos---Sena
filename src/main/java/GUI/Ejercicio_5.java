    public String imprimirCubosInteresantes(String a, javax.swing.JTextArea textArea) {
    long limite = Long.parseLong(a);
    
    for (long num = 1; num <= limite; num++) {
        long acu = 0;
        long tempNum = num;  // Guardar el nÃºmero original para comparar al final
        int longitud = String.valueOf(num).length();

        while (tempNum > 0) {
            long digito = tempNum % 10;
            acu += Math.pow(digito, longitud);
            tempNum /= 10;
        }

        if (acu == num) {
            textArea.append("\n" + num);
        }
    }
    return "";
}


    
    
    
